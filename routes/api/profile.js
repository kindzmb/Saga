// @route   PUT api/profile/experience
// @desc    Add profile experience
// @access  Private (Private routes need a token)
router.put(
  '/experience',
  [
    auth,
    [
      check('title', 'Title is required').not().isEmpty(),
      check('company', 'Company is required').not().isEmpty(),
      check('from', 'From date is required').not().isEmpty(),
    ],
  ],
  async (req, res) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({ errors: errors.array() });
    }

    const {
      title,
      company,
      location,
      from,
      to,
      current,
      description,
    } = req.body;

    const newExp = {
      title,
      company,
      location,
      from,
      to,
      current,
      description,
    };

    try {
      const profile = await Profile.findOne({ user: req.user.id });

      // unshift is adding the new experience data submitted to the user to the beginning of the profile experience object
      profile.experience.unshift(newExp);

      await profile.save();

      res.json(profile);
    } catch (err) {
      console.error(err.message);
      res.status(500).send('Server Error');
    }
  }
);

// @route   DELETE api/profile/experience/:exp_id
// @desc    Delete experience from profile
// @access  Private (Private routes need a token)
router.delete('/experience/:exp_id', auth, async (req, res) => {
  try {
    // Get the profile of the logged in user
    const profile = await Profile.findOne({ user: req.user.id });

    // Get remove index of the to be deleted experience
    const removeIndex = profile.experience
      .map(item => item.id)
      .indexOf(req.params.exp_id);

    // use the remove index to splice that experience from the list of experiences
    profile.experience.splice(removeIndex, 1);

    // Save the profile to MongoDB with the updated experiences list
    await profile.save();

    // send back a response to the front end
    res.json(profile);
  } catch (err) {
    console.error(err.message);
    res.status(500).send('Server Error');
  }
});

